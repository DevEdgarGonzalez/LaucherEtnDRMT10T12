package com.actia.multimedia;

public class Properties_WebHelpMultimedia {
    private String url;
    private int enableNavigation;
    private int showUrl;
    private int showTitle;

    public Properties_WebHelpMultimedia() {
    }



    public String getUrl() {
        return url;
    }



    public void setUrl(String url) {
        this.url = url;
    }

    public int getEnableNavigation() {
        return enableNavigation;
    }

    public void setEnableNavigation(int enableNavigation) {
        this.enableNavigation = enableNavigation;
    }

    public int getShowUrl() {
        return showUrl;
    }

    public void setShowUrl(int showUrl) {
        this.showUrl = showUrl;
    }

    public int getShowTitle() {
        return showTitle;
    }

    public void setShowTitle(int showTitle) {
        this.showTitle = showTitle;
    }
}
